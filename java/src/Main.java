public class Main {

    public static void main(String[] args) {
        for(int i=0;i<5;i++){
            System.out.print(i);
        }
        int i=0;
        while(i<5){
            System.out.print(i);
            i++;
        }
        i=0;
        do{
            System.out.print(i);
            i++;
        }while(i<5);
        System.out.println("Hello World!");
    }
}
